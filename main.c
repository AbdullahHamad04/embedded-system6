uint8_t SPI_Transmit(uint8_t data) {
    while (!(SPI1->SR & SPI_SR_TXE));  // wait until TX buffer empty
    SPI1->DR = data;
    while (!(SPI1->SR & SPI_SR_RXNE)); // wait until RX complete
    return SPI1->DR;
}

uint8_t SPI_Receive(uint8_t addr) {
    uint8_t value;

    GPIOE->ODR &= ~GPIO_ODR_OD3;      // CS low
    SPI_Transmit(addr | 0x80);        // set read bit (MSB)
    value = SPI_Transmit(0x00);       // dummy byte to clock in response
    GPIOE->ODR |= GPIO_ODR_OD3;       // CS high

    return value;
}

void SPI_Write(uint8_t addr, uint8_t data) {
    GPIOE->ODR &= ~GPIO_ODR_OD3;      // CS low
    SPI_Transmit(addr & 0x7F);        // Clear MSB for write
    SPI_Transmit(data);
    GPIOE->ODR |= GPIO_ODR_OD3;       // CS high
}

int8_t x_val, y_val, z_val;

void LIS3DSH_Init() {
    // CTRL_REG4 = 0x20: Enable X, Y, Z + ODR 50Hz (0x47 = 0b01000111)
    SPI_Write(0x20, 0x47);
}

void LIS3DSH_ReadAxes() {
    x_val = (int8_t)SPI_Receive(OUT_X);
    y_val = (int8_t)SPI_Receive(OUT_Y);
    z_val = (int8_t)SPI_Receive(OUT_Z);
}

uint8_t LIS3DSH_WhoAmI() {
    return SPI_Receive(WHO_AM_I);
}

